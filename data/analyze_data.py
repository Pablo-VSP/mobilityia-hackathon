import pandas as pd

# Lee el archivo consolidado (ejecutar desde carpeta data/)
all_df = pd.read_parquet('consolidated_telemetry.parquet')
print(f'Total registros: {all_df.shape[0]:,}')
print(f'Columnas: {all_df.shape[1]}')
print()
print('=== RESUMEN GENERAL ===')
print(f'Buses unicos: {all_df["Autobus"].nunique()}')
print(f'Viajes unicos: {all_df["VIAJE_ID"].nunique()}')
print(f'Operadores unicos: {all_df["Operador_Cve"].nunique()}')
print(f'SPNs unicos: {all_df["EVENTO_SPN"].nunique()}')
print(f'Rutas unicas: {all_df["VIAJE_RUTA"].nunique()}')
print(f'Rango fechas: {all_df["EVENTO_FECHA"].min()} a {all_df["EVENTO_FECHA"].max()}')
print()
print('=== SPNs presentes ===')
spn_counts = all_df.groupby(['EVENTO_SPN','EVENTO_DESCRIPCION']).size().reset_index(name='registros').sort_values('registros', ascending=False)
for _, row in spn_counts.iterrows():
    print(f'  SPN {row["EVENTO_SPN"]:>5}: {row["EVENTO_DESCRIPCION"]:<50} ({row["registros"]:>10,} registros)')
print()
print('=== RUTAS ===')
ruta_counts = all_df['VIAJE_RUTA'].value_counts()
for ruta, cnt in ruta_counts.items():
    print(f'  {ruta}: {cnt:,}')
print()
print('=== ESTADISTICAS EVENTO_VALOR por SPN ===')
stats = all_df.groupby(['EVENTO_SPN','EVENTO_DESCRIPCION'])['EVENTO_VALOR'].describe()
print(stats.to_string())
print()
print('=== RANGO TEMPORAL ===')
print(f'Fecha min: {all_df["EVENTO_FECHA_HORA"].min()}')
print(f'Fecha max: {all_df["EVENTO_FECHA_HORA"].max()}')
print()
print('=== BUSES (top 20 por registros) ===')
bus_counts = all_df['Autobus'].value_counts().head(20)
for bus, cnt in bus_counts.items():
    print(f'  Bus {bus}: {cnt:,} registros')
